public class Event {

    public String eventName;
    public String startTime;
    public String endTime;
    public String startDay;
    public String endDay;


    public Event(String name, String st, String et, String sd, String ed){
        eventName = name;
        startTime = st;
        endTime = et;
        startDay = sd;
        endDay = ed;
    }










}
